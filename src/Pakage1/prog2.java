package Pakage1;

public class prog2 {
	
	public static void main(String args[])
	{
		int i=5;
		//short hand writing
		i+=1;
		System.out.println(i);
		
		//post increment
		int j = i++;
		System.out.println(i);
		System.out.println(j);
		
		//pre increment
		++i;
		System.out.println(i);
		
		//post decrement
		i--;
		System.out.println(i);
		
		//pre decrement
		--i;
		System.out.println(i);
	}
}
